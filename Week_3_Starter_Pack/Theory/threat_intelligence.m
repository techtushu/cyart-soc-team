# Threat Intelligence & Threat Hunting

## Levels of Threat Intelligence
- **Tactical**: IoCs (IP addresses, hashes, domains)
- **Operational**: TTPs (tools, attack patterns)
- **Strategic**: Big-picture trends for executives

## Key Sources
- OSINT (VirusTotal, AbuseIPDB, AlienVault OTX, etc.)
- CTI feeds (STIX/TAXII based)
- Commercial vendors

## MITRE ATT&CK
- Adversary behavior framework
- Maps Tactics → Techniques → Procedures

## IoC vs IoA
- **IoC**: Evidence an attack has occurred
- **IoA**: Evidence an attack is in progress

